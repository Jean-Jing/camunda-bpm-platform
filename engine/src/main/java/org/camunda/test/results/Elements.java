package org.camunda.test.results;

import java.util.ArrayList;
import java.util.List;
import org.w3c.dom.Document;
import org.w3c.dom.Element;
import org.w3c.dom.Node;
import org.w3c.dom.NodeList;

public class Elements {

  public static List<Element> getTestElements(Document document) {
    return getElements(document, "test");
  }

  private static List<Element> getElements(Document document, String elementTagName) {
    List<Element> result = new ArrayList<>();

    NodeList nodeList = document.getElementsByTagName(elementTagName);
    for (int i = 0; i < nodeList.getLength(); i++) {
      Node node = nodeList.item(i);
      if (node.getNodeType() == Node.ELEMENT_NODE) {
        result.add((Element) node);
      }
    }

    return result;
  }

}